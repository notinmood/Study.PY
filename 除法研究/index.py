# 本文档演示 `/`、'//'和'%' 三个符号在除法中的意义
# 下面是各种除法的示例

"""
1. `/`:除法
2. `//`:向下取整（比如4.5向下取整就是4；-4.5向下取整就是-5。）
3. `%`:取模（取余数）
"""

print(9 / 2)  # 4.5
print(9 // 2)  # 4
print(9 % 2)  # 1

print(-9 / 2)  # -4.5
print(-9 // 2)  # -5
print(-9 % 2)  # -1
